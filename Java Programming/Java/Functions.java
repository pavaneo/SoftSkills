public class Functions {
    

    public static void main(String[] args) {
        System.out.println();
        System.out.println("Welcome to Java Functions\n");
        function1();
        function2();
        System.out.println();
    }

    public static void function1() {
        System.out.println("Printing function 1");
    }

    public static int function2() {
        System.out.println("Printing function 2 and returning 0");
        return 0;
    }
}
